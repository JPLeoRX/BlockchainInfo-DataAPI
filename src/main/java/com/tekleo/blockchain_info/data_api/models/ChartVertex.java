package com.tekleo.blockchain_info.data_api.models;

public class ChartVertex {
    private long x;
    private double y;

    // Getters
    //------------------------------------------------------------------------------------------------------------------
    public long getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    //------------------------------------------------------------------------------------------------------------------



    // Others
    //------------------------------------------------------------------------------------------------------------------
    @Override
    public String toString() {
        return "ChartVertex{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
    //------------------------------------------------------------------------------------------------------------------
}